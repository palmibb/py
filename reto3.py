# Para esto, la copa minitas lo contrato a usted para diseñar un programa que reciba como entrada la planilla de registro de goles anotados en
# el encuentro X (Recuerde que la planilla trae la inicial del nombre del jugador que anoto gol durante el encuentro separados por espacios) y
# deberá imprimir dos líneas, la primera deberá ser la inicial de los jugadores que anotaron gol y en la segunda línea se debe imprimir el conteo
# de goles consecutivos por un mismo jugador (Todos van separados por espacios en blanco).

# Entrada

# La entrada consta de una sucesión de caracteres separados por espacio que correspond a la planilla de registro de goles anotados en el
# encuentro.

# Salida

# La salida consta de dos líneas: dos líneas, la primera deberá ser la inicial de los jugadores que anotaron gol y en la segunda línea se debe
# imprimir el conteo de goles consecutivos por un mismo jugador. Las dos lineas separados ppor espacios.

 

 

 

